--For the camo stuff I can just give you a quick rundown of it, along with my notes. 
--The gist is each string of numbers between curly braces sets what surfaces and objects contribute to your camo index when wearing a uniform. 
--0 means the obvious and 50 is the bonus amount you get to your camo index. 
--The default values are set in a way that the KJP devs treat bonuses like booleans for some reason. 
--I guess because of the lack of an index display on the HUD. You seem to only get one type of bonus at once, and it seems to be based on what type of surface it is. 
--Decals > surface texture > 3d model as far a priority goes. 
--At least, that seems to be the case for rocks in Africa (soil on top of rocks prevents the rocky surface bonus), rivers with desert fox-type soil, and puddles on roads.
--
--Additionally the bonus appears to only apply when crouched, or at least that's the only time it makes a difference. 
--Daytime crouch visibility: 70m wrong camo and 45m right camo. Nighttime crouch visibility: 35m wrong camo and 15m right camo. 
--You might be able to further reduce guard vision with the bonus by increasing values above 50. 
--But I haven't tried that so I wouldn't know what the limit is, aside from the amount stated within enemyparam or whatever the lua file with guard vision is named.
--
--The parameters in the outfit camouflage file are basically broad categories that certain surfaces fall under. 
--So if a neon green surface falls under parameter Sand A it probably wouldn't be possible to move it under a more appropriate parameter.

